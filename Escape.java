public class Escape
{
	public static void main(String args[])
	{
		System.out.println("Hey\nHi");
		System.out.println("One\rTwo");
		System.out.println("A\bB");
		System.out.println("Huzaifa\tIs\tNaughty");
		System.out.println("\"Huzaifa\"");
		System.out.println("\'Virus\'");
		System.out.println("\\Khaad\tFactory\\");
	}
}